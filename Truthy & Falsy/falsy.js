// false
console.log( false ? 'false/Yes': 'false/No' );

// "" (or '')
console.log( "" ? '""/Yes': '""/No' );

// 0 (or -0)
console.log( 0 ? '0/Yes': '0/No' );

// NaN
console.log( NaN ? 'NaN/Yes': 'NaN/No' );

// null
console.log( null ? 'null/Yes': 'null/No' );

// undefined
console.log( undefined ? 'undefined/Yes': 'undefined/No' );